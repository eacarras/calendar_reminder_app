class Reminder {

  constructor(title, message, day, month, year, hour, city, color) {
    this.title = title;
    this.message = message;
    this.day = day;
    this.month = month;
    this.year = year;
    this.hour = hour;
    this.city = city;
    this.color = color;
  }
}

export default Reminder;